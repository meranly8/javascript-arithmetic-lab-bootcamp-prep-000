function add(a,b) {
  return a + b
}

function subtract(a,b) {
  return a - b 
}

function multiply(a,b) {
  return a * b
}

function divide(a,b) {
  return a / b
}

function inc(n) {
  n++
  return n
}

function dec(n) {
  n--
  return n
}

function makeInt(n) {
  parseInt(n)
  return n
}
makeInt('0x2328')

function preserveDecimal(n) {
  parseFloat(n)
  return n
}
preserveDecimal('2.222')










